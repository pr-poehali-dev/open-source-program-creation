function getBotAnswer(text: string, legalCase: LegalCase): { answer: string; tag: string; newCase?: Partial<LegalCase> } {
  // Правовая база
  for (const item of LEGAL_KB) {
    const m = text.match(item.pattern);
    if (m) {
      const answer = typeof item.answer === "function" ? item.answer(m) : item.answer;
      return { answer, tag: item.tag };
    }
  }
  // Инциденты
  for (const item of INCIDENT_KB) {
    if (item.pattern.test(text)) return { answer: item.answer, tag: "ИНЦИДЕНТ" };
  }
  // Общее
  for (const item of GENERAL_KB) {
    if (item.pattern.test(text)) return { answer: item.answer, tag: "ЕЦСУ" };
  }

  // Логика контекстного диалога
  if (legalCase.stage === "accusation") {
    return {
      answer: `📋 **Продолжение по делу: «${legalCase.subject}»**\n\nЗафиксировал ваши доводы. Правовая позиция обвинения:\n\n1. **Деяние установлено:** факты соответствуют составу инкриминируемой нормы\n2. **Доказательственная база:** требуется подтвердить причинно-следственную связь\n3. **Квалификация:** прошу уточнить — умышленное или неосторожное деяние?\n\nВведите «итог» — сформирую предварительный вывод по делу.`,
      tag: "ОБВИНЕНИЕ",
      newCase: { facts: [...legalCase.facts, text] },
    };
  }

  return { answer: DEFAULT_ANSWER, tag: "ЕЦСУ" };
}

